#this code converts user input into uppercase letters

#prompt user to input a word or sentence and store it as variable "user_input"
user_input = input("please enter a word or phrase: ")

#create an empty string variable to store the uppercase version of the user input
uppercase_output = ""

#convert input to uppercase letters and store it as the "uppercase_output" variable
uppercase_output = user_input.upper()

#print the uppercase output
print(uppercase_output)
